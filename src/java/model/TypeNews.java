package model;

/**
 *
 * @author Admin
 */
public class TypeNews {
    private int typeId;
    private String typeName;

    public TypeNews() {
    }

    public TypeNews(int typeId, String typeName) {
        this.typeId = typeId;
        this.typeName = typeName;
    }

    public int getTypeId() {
        return typeId;
    }

    public void setTypeId(int typeId) {
        this.typeId = typeId;
    }

    public String getTypeName() {
        return typeName;
    }

    public void setTypeName(String typeName) {
        this.typeName = typeName;
    }

    @Override
    public String toString() {
        return "TypeNews{" + "typeId=" + typeId + ", typeName=" + typeName + '}';
    }
    
}
